import type { SlashCommand } from '../types.js';

const streamingBusy = 'Agent is currently streaming. Wait for it to finish, then try again.';

export const doctorCommands: SlashCommand[] = [
  {
    name: 'doctor',
    aliases: ['dr'],
    help: 'diagnose your development environment',
    usage: '/doctor',
    run: (_arg, ctx) => {
      if (ctx.isStreaming) {
        ctx.sys(streamingBusy);
        return;
      }

      ctx.send(
        [
          'Run a comprehensive diagnostic check of the development environment and produce a health report.',
          '',
          'For each check, mark the result as [PASS], [FAIL], or [WARN].',
          '',
          '--- Check 1: Node.js ---',
          'Run: node --version',
          'PASS if version >= 18.0.0. FAIL otherwise or if not installed.',
          '',
          '--- Check 2: npm ---',
          'Run: npm --version',
          'PASS if any version is returned. FAIL if not installed.',
          '',
          '--- Check 3: pnpm (optional) ---',
          'Run: pnpm --version',
          'PASS if installed (note version). WARN if not installed (optional tool).',
          '',
          '--- Check 4: Git ---',
          'Run: git --version',
          'PASS if installed. FAIL otherwise.',
          '',
          '--- Check 5: Settings file ---',
          'Check if ~/.coder/settings.json exists.',
          'If it exists, read it and validate:',
          '  - File is valid JSON (parse without error)',
          '  - Contains a "model_list" array with at least one entry',
          '  - Each model_list entry has a "model" array and a "provider" field',
          'PASS if all validations pass. FAIL with specific reason otherwise.',
          'If the file does not exist, FAIL with: "Run the app once to generate default settings."',
          '',
          '--- Check 6: Default model ---',
          'From settings.json, extract "default_model" (format: "provider/model-name").',
          'Verify the provider matches a model_list entry and the model name exists in that provider\'s model array.',
          'PASS if configured and matches. FAIL if default_model is missing or does not match any model_list entry.',
          '',
          '--- Check 7: API key ---',
          'For the resolved default model\'s provider entry, check the "auth_token_env" value:',
          '  - If the value starts with "YOUR_" or equals "LOCAL_NO_KEY": FAIL — it is a placeholder. Tell the user to update ~/.coder/settings.json',
          '  - If the value is empty or missing: FAIL — no API key configured',
          '  - If the value looks like a real key (non-empty, non-placeholder): PASS — show masked version (first 4 ... last 4 chars)',
          '',
          '--- Check 8: API endpoint connectivity ---',
          'For the resolved default model\'s provider, get the "base_url".',
          'Test connectivity by using curl, fetch, or a simple HTTP HEAD/GET to the base URL.',
          'PASS if the endpoint responds (any HTTP status, even 401/403 is fine — it means the server is reachable).',
          'FAIL if connection is refused, DNS fails, or request times out (set a 10-second timeout).',
          'If no base_url is configured, mark as WARN with "No endpoint to test."',
          '',
          '--- Output format ---',
          'Produce the report in this exact format:',
          '',
          '```',
          '=== Environment Health Report ===',
          '',
          '  [PASS] Node.js: v22.3.0',
          '  [PASS] npm: 10.8.0',
          '  [WARN] pnpm: not installed',
          '  [PASS] Git: 2.45.0',
          '  [PASS] Settings: ~/.coder/settings.json exists and is valid JSON (3 providers)',
          '  [PASS] Default model: deepseek/deepseek-v4-pro',
          '  [FAIL] API key: YOUR_DEEPSEEK_API_KEY is a placeholder — update auth_token_env in ~/.coder/settings.json',
          '  [PASS] API endpoint: https://api.deepseek.com/anthropic responded (HTTP 200)',
          '',
          'Summary: 5 passed, 1 failed, 2 warnings',
          '```',
          '',
          '--- Remediation ---',
          'After the report, if there are any FAIL results, add a "Remediation" section with specific, actionable steps to fix each failure.',
        ].join('\n'),
      );
    },
  },
];
